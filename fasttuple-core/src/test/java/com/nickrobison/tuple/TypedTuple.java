public interface TypedTuple {
    byte aByte();
    void aByte(byte aByte);
    char aChar();
    void aChar(char aChar);
    int aInt();
    void aInt(int aInt);
    short aShort();
    void aShort(short aShort);
    float aFloat();
    void aFloat(float aFloat);
    long aLong();
    void aLong(long aLong);
    double aDouble();
    void aDouble(double aDouble);
}
